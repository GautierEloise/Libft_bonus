#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (0);
	while (lst->next)
	{
		//printf("cherche le last : %s\n", (char *)lst->next->content);
		lst = lst->next;
	}
	return (lst);
}
